# Převěď celsia na fahrenheit

TeplotaCelsia = float(input("Zadej zde svojí teplotu: ")) # Vytvoříme input který bude obsahovat naší proměnou
PrevodNaFahrenheit = (TeplotaCelsia*9/5) + 32    # Vytvoříme proměnou která vypočítá celsia na fahrenheit

print("Převod je:",(PrevodNaFahrenheit)) # Vypíšeme